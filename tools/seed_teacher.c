#define PATTERN_COUNT (sizeof(patterns) / sizeof(patterns[0]))

int main(int argc, char* argv[]) {
    const char* seed_path = argc > 1 ? argv[1] : "memory_seed.dat";
    
    printf("╔═══════════════════════════════════════════╗\n");
    printf("║     PivotMind 对话种子注入工具           ║\n");
    printf("╚═══════════════════════════════════════════╝\n\n");

    // 创建记忆系统
    MemorySystem* memory = memory_system_create(100, 500, 5000);
    if (!memory) {
        printf("[错误] 无法创建记忆系统\n");
        return 1;
    }
    
    // 加载现有种子（如果有）
    int loaded = memory_load_seed(memory, seed_path);
    if (loaded > 0) {
        printf("[加载] 已加载 %d 条现有种子\n", loaded);
    } else {
        printf("[加载] 无现有种子，创建新种子\n");
    }
    
    // 批量注入对话模式
    int new_count = 0;
    int skip_count = 0;
    for (int i = 0; i < PATTERN_COUNT; i++) {
        char key[256];
        snprintf(key, sizeof(key), "response:%s", patterns[i][0]);
        
        // 检查是否已存在
        MemoryEntry* existing = memory_retrieve(memory, key);
        if (existing) {
            skip_count++;
            continue;
        }
        
        memory_store(memory, key, 
                    strdup(patterns[i][1]),
                    strlen(patterns[i][1]) + 1,
                    MEMORY_TYPE_STRING, 0.9f);
        new_count++;
    }
    
    printf("\n[注入结果]\n");
    printf("  注入: %d 条\n", new_count);
    printf("  跳过(已存在): %d 条\n", skip_count);
    printf("  总计: %d 条\n", new_count + skip_count);
    
    // 保存种子
    int saved = memory_save_seed(memory, seed_path);
    if (saved > 0) {
        printf("\n✓ 已保存到 %s (%d 条)\n", seed_path, saved);
    } else {
        printf("\n× 保存失败\n");
    }
    
    // 清理
    memory_system_destroy(memory);
    return 0;
}
