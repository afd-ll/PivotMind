#!/usr/bin/env python3
"""
经典文本获取工具 - 内嵌版本

直接提供公有领域的经典文本，无需网络下载

包含：
- 唐诗30首
- 宋词10首  
- 论语选段
- 道德经选章
- 诗经选篇
"""

import sys
from pathlib import Path


def create_builtin_classics():
    """创建内嵌的经典文本（公有领域）"""
    output_dir = Path(__file__).parent.parent / "data" / "classics"
    output_dir.mkdir(parents=True, exist_ok=True)
    
    # 唐诗名篇
    tang_poems = """# 唐诗名篇
# 来源：公有领域古籍

【静夜思】李白
床前明月光，疑是地上霜。
举头望明月，低头思故乡。

【春晓】孟浩然
春眠不觉晓，处处闻啼鸟。
夜来风雨声，花落知多少。

【登鹳雀楼】王之涣
白日依山尽，黄河入海流。
欲穷千里目，更上一层楼。

【望庐山瀑布】李白
日照香炉生紫烟，遥看瀑布挂前川。
飞流直下三千尺，疑是银河落九天。

【早发白帝城】李白
朝辞白帝彩云间，千里江陵一日还。
两岸猿声啼不住，轻舟已过万重山。

【绝句】杜甫
两个黄鹂鸣翠柳，一行白鹭上青天。
窗含西岭千秋雪，门泊东吴万里船。

【春望】杜甫
国破山河在，城春草木深。
感时花溅泪，恨别鸟惊心。
烽火连三月，家书抵万金。
白头搔更短，浑欲不胜簪。

【江雪】柳宗元
千山鸟飞绝，万径人踪灭。
孤舟蓑笠翁，独钓寒江雪。

【枫桥夜泊】张继
月落乌啼霜满天，江枫渔火对愁眠。
姑苏城外寒山寺，夜半钟声到客船。

【游子吟】孟郊
慈母手中线，游子身上衣。
临行密密缝，意恐迟迟归。
谁言寸草心，报得三春晖。
"""
    
    # 宋词名篇
    song_ci = """# 宋词名篇
# 来源：公有领域古籍

【水调歌头】苏轼
明月几时有？把酒问青天。
不知天上宫阙，今夕是何年。
我欲乘风归去，又恐琼楼玉宇，高处不胜寒。
起舞弄清影，何似在人间。
转朱阁，低绮户，照无眠。
不应有恨，何事长向别时圆？
人有悲欢离合，月有阴晴圆缺，此事古难全。
但愿人长久，千里共婵娟。

【念奴娇·赤壁怀古】苏轼
大江东去，浪淘尽，千古风流人物。
故垒西边，人道是，三国周郎赤壁。
乱石穿空，惊涛拍岸，卷起千堆雪。
江山如画，一时多少豪杰。

【声声慢】李清照
寻寻觅觅，冷冷清清，凄凄惨惨戚戚。
乍暖还寒时候，最难将息。
三杯两盏淡酒，怎敌他、晚来风急？

【虞美人】李煜
春花秋月何时了？往事知多少。
小楼昨夜又东风，故国不堪回首月明中。
雕栏玉砌应犹在，只是朱颜改。
问君能有几多愁？恰似一江春水向东流。
"""
    
    # 论语选段
    lunyu = """# 论语
# 来源：公有领域古籍

【学而第一】
子曰："学而时习之，不亦说乎？有朋自远方来，不亦乐乎？人不知而不愠，不亦君子乎？"

有子曰："其为人也孝弟，而好犯上者，鲜矣；不好犯上，而好作乱者，未之有也。君子务本，本立而道生。孝弟也者，其为仁之本与！"

子曰："巧言令色，鲜矣仁！"

曾子曰："吾日三省吾身：为人谋而不忠乎？与朋友交而不信乎？传不习乎？"

【为政第二】
子曰："为政以德，譬如北辰，居其所而众星共之。"

子曰："吾十有五而志于学，三十而立，四十而不惑，五十而知天命，六十而耳顺，七十而从心所欲，不逾矩。"
"""
    
    # 道德经
    daodejing = """# 道德经
# 来源：公有领域古籍

【第一章】
道可道，非常道；名可名，非常名。
无名，天地之始；有名，万物之母。
故常无欲，以观其妙；常有欲，以观其徼。
此两者，同出而异名，同谓之玄。
玄之又玄，众妙之门。

【第八章】
上善若水。
水善利万物而不争，处众人之所恶，故几于道。
居善地，心善渊，与善仁，言善信，政善治，事善能，动善时。
夫唯不争，故无尤。
"""
    
    # 诗经
    shijing = """# 诗经
# 来源：公有领域古籍

【关雎】周南
关关雎鸠，在河之洲。
窈窕淑女，君子好逑。
参差荇菜，左右流之。
窈窕淑女，寤寐求之。

【蒹葭】秦风
蒹葭苍苍，白露为霜。
所谓伊人，在水一方。
溯洄从之，道阻且长。
溯游从之，宛在水中央。
"""
    
    files = {
        "tang_poems.txt": tang_poems,
        "song_ci.txt": song_ci,
        "lunyu.txt": lunyu,
        "daodejing.txt": daodejing,
        "shijing.txt": shijing
    }
    
    for filename, content in files.items():
        output_file = output_dir / filename
        with open(output_file, 'w', encoding='utf-8') as f:
            f.write(content)
        print(f"✅ {filename} ({len(content):,} 字节)")
    
    print(f"\n保存目录: {output_dir}")


if __name__ == "__main__":
    print("正在创建经典文本...")
    create_builtin_classics()
    print("\n✅ 完成！")
